import UIKit

extension UIColor {
  
  public class var mainDarkGreyColor: UIColor {
    return UIColor(named: "mainDarkGreyColor")!
  }
  
  public class var mainLightGreyColor: UIColor {
    return UIColor(named: "mainLightGreyColor")!
  }
  
  public class var mainRedColor: UIColor {
    return UIColor(named: "mainRedColor")!
  }
  
  public class var mainBlackColor: UIColor {
    return UIColor(named: "mainBlackColor")!
  }
  
  public class var mainBlueColor: UIColor {
    return UIColor(named: "mainBlueColor")!
  }
  
  public class var backgroundColor: UIColor {
    return UIColor(named: "backgroundColor")!
  }
  
}
